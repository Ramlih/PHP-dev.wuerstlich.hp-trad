Unser Motto

"[würstlich] ...fürstlich genießen - Der Imbiss für den gehobenen Anspruch
setzt uns hohe Maßstäbe.

Zufriedene Kunden
... kommen gern immer wieder. Das beweisen die zahlreiche Stammgäste vom
[würstlich] in Magdeburg, Oschersleben  und mittlerweile auch in Haldensleben.

In einem gemütlichen Ambiente mit überdachten Sitzmöglichkeiten können unsere Gäste aus einem reichhaltigen
Speisen- und Getränkeangebot wählen.

Alle unsere Filialen bieten ausreichend Parkmöglichkeiten in unmittelbarer
Nähe.

Ein reichhaltiges Speisen- und Getränkeangebot
... ist sowohl für den kleinen als auch für den großen Hunger stets vorhanden.
Dazu gehören die typischen Klassiker wie Bratwurst, Currywurst, Frikadelle,
Hamburger und Cheesburger. Je nach Standort gibt es entweder täglich, oder an mehreren
Tagen der Woche wechselnde Tagesgerichte.
In Oschersleben hat sich dazu im Gegensatz der "Burger des Monats" durchgesetzt.

Premiumqualität, Frische und Geschmack
... stellen wir durch die Verwendung täglich frischer Zutaten sicher.
Wir beziehen unsere Produkte täglich frisch von einem regionalen, von uns ausgesuchten
Fleischerfachbetrieb. Wir erreichen durch die Verwendung von überwiegend regional
hergestellten und geprüften Produkten für unsere, nach hauseigenen Rezepten, täglich
frisch zubereiteten Salate und Soßen einen hohen Qualitätsstandard.

Kundenorientierte Öffnungszeiten
Unsere Öffnungszeiten passen wir an die Gegebenheiten des jeweiligen Standortes an.

Freundliche Mitarbeiter
... sind eine Selbstverständlichkeit. Unsere Kunden sollen sich bei uns wohlfühlen.
Ein Lächeln, ein aufmunterndes Wort und eine schnelle, zuvorkommende Bedienung sind deshlab unsere Maxime.